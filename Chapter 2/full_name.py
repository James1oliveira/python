# Store the first name
first_name = "ada"

# Store the last name
last_name = "lovelace"

# Combine first and last name into a full name using an f-string
full_name = f"{first_name} {last_name}"

# Create a personalized greeting message using the title-cased full name
message = f"Hello, {full_name.title()}!"

# Print the greeting message
print(message)
