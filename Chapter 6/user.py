# Create a dictionary to store information about a user
user_0 = {
    'username': 'efermi',
    'first': 'enrico',
    'last': 'fermi',
}

# Loop through each key-value pair in the dictionary
for key, value in user_0.items():
    # Print the key
    print(f"\nKey: {key}")
    # Print the corresponding value
    print(f"Value: {value}")
